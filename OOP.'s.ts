// Object Oriented Programming(OOP)

// Class Definaion
class Arithmatic
{
	// Characteristics
	No1:number;
	No2:number;

	Addition()	// Behaviour
	{
		var Ans:number = 0;	    // Local Variable
		Ans = this.No1 + this.No2;
		return Ans;
	}

	Subtraction()	// Behaviour
	{
		var Ans:number = 0;	    // Local Variable
		Ans = this.No1 - this.No2;
		return Ans;
	}
}

